package Tema_3_Estructuras_de_control_y_Muchos_usos;
/*
Programa que hace una colunmna del 1 al 10 ascendiente y descendiente
 */
public class Ejercicio07_Columna_del_1_al_10_con_for {
    
  public static void main (String [] args){
      
  
    for(int k=1,j=10;k<=10&&j>0;k++,j--){
      System.out.println(k+"  "+j);
        } 
    }
}